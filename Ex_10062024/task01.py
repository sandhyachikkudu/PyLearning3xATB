# 1. Difference between the = and == operators
#= operator is a assignment operator. It assigns the value on the right to the variable on the left.
#== operator is a comparison operator. It compares the values on the left and right and returns True if they are equal and False otherwise.

# Example
a = input("Enter The first number\t")
b = input("Enter The second number\t")
a = int(a)
b = int(b)
print(a == b)


# 2 ** operator use in python
# ** operator is used to raise a number to a power.
import math
x = input("Enter the number\t")
x = int(x)
print(x ** 2)
print(math.pow(x,2))



# ^ operator use in python
# ^ is a bitwise operator. It performs a logical exclusive OR operation on the two operands.
num = input("Enter the number\t")
num = int(num)
print(num ^ 2)




